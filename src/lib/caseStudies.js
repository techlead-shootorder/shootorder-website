export function getCaseStudyBySlug(serviceSlug, caseSlug) {
  return allCaseStudies.find(
    study => study.slug === caseSlug && study.id === serviceSlug
  );
}

export function getCaseStudiesByService(serviceSlug) {
  return allCaseStudies.filter(study => study.id === serviceSlug);
}

export function getAllCaseStudySlugs() {
  return allCaseStudies.map(study => ({
    service: study.id,
    slug: study.slug
  }));
}